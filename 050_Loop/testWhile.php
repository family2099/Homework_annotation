<?php
// while 迴圈與for 迴圈不同的是初始值變數在迴圈外先設定
//A模式為初始值在迴圈外就設定完成
$iSum = 0;
$i = 1;//準備資料設定初值
while ($i <= 10)//條件成立的話執行以下,不成立跳出while迴圈繼續執行下面的程式
{
	$iSum += $i;
	$i += 1;
}
echo $iSum;

echo "<hr>";
//B模式為初始值先都設為0,再到迴圈內設定初始值
$iSum = 0;
$i = 0;
while ($i < 10)
{
	
	$i++;//準備資料設定初值
	$iSum += $i;	
}
echo $iSum
?>